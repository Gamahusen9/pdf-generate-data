            <h4 style="font-weight: bold; text-align: center;">PASAL 9</h4>
            <h4 style="font-weight: bold; text-align: center;">Tambah Kurang Pekerjaan</h4>

            <ol class="content-text style=margin-left: 40px;">
                <li>Jika di kemudian hari dalam proses pelaksanaan konstruksi terdapat pekerjaan tambah dan pekerjaan kurang yang berhubangan dengan biaya tambahan (Add On) dan/atau Harga Paket Pembangunan Rumah â€“ BRO akibat perubahan spesifikasi material bangunan atau gambar kerja, maka hal tersebut akan diatur dalam addendum tersendiri.</li>

                <li>Setiap pekerjaan tambah atau kurang harus melalui dan/atau dari <b>PIHAK PERTAMA</b>.</li>


                <li>Pekerjaan tambah atau kurang yang melalui <b>PIHAK KEDUA</b> akibat masalah teknis, harus diberitahukan pada <b>PIHAK PERTAMA</b>.</li>


                <li>Semua hal-hal yang terkait dengan pekerjaan tambah -kurang, harus ditandai dengan adanya Berita Acara dan/atau addendum kontrak (apabila diperlukan) serta diketahui oleh kedua belah pihak.</li>
            </ol>

            <h4 style="font-weight: bold; text-align: center;">PASAL 10</h4>
            <h4 style="font-weight: bold; text-align: center">Garansi Perjanjian</h4>

            <ol class="content-text style=margin-left: 40px;">
                <li><b>PIHAK KEDUA</b> bersedia melakukan perbaikan dan/atau penggantian atas pekerjaan tersebut apabila tidak berfungsi dengan baik atau apabila ada kerusakan pada hasil pekerjaan kami yang terbukti oleh pekerja, material dan pengoperasian alat kami dalam periode yang ditetapkan pada poin selanjutnya. </li>

                <li><b>PIHAK KEDUA</b> memberikan jaminan atau garansi hasil pekerjaan kepada <b>PIHAK PERTAMA</b> selama 1 (satu) tahun setelah Berita Acara Serah Terima (BAST) ditanda tangani.</li>

                <li><b>PIHAK KEDUA</b> menerbitkan sertifikat garansi dan/atau buku garansi untuk <b>PIHAK PERTAMA</b>, yang didalamnya disebutkan tentang data umum pekerjaan, syarat dan ketentuan klaim garansi dan jugaterdapat komponen-komponen yang ter-cover garansi seperti daftar kuantitas (Bill of Quantity) dan spesifikasi teknis lainnya.</li>

                <li>Garansi pekerjaan tidak berlaku apabila kerusakan dan/atau  tidak berfungsi dengan baik terjadi akibat dari kelalaian dan/atau kesalahan dalam pengoperasian, perubahan, penambahan atau pengurangan komponen pekerjaan konstruksi. Serta juga yang diakibatkan oleh rusak akibat bahan kimia lainnya, bencana alam atau keadaan memaksa (Force Majeur) sebagaimana yang dicantumkan pada <b>PASAL 11</b> (sebelas).</li>

                <li>Bila setelah habisnya masa garansi dan/atau ketentuan yang dicantumkan pada poin 4 (empat) sebelumnya, ada penambahan, perubahan, perbaikan pekerjaan yang dilakukan <b>PIHAK KEDUA</b> atas persetujuan <b>PIHAK PERTAMA</b>, maka akan diberlakukannya biaya pekerjaan sesuai dengan bobot pekerjaan dan harga material saat itu.</li>

                <li>Bila setelah habisnya masa garansi dan/atau ketentuan yang dicantumkan pada poin 4 (empat) sebelumnya, ada penambahan, perubahan, perbaikan pekerjaan yang dilakukan <b>PIHAK KEDUA</b> atas persetujuan PIHAK PERTAMA, maka akan diberlakukannya biaya pekerjaan sesuai dengan bobot pekerjaan dan harga material saat itu.</li>

            </ol>

                        <h4 style="font-weight: bold; text-align: center;">PASAL 11</h4>
            <h4 style="font-weight: bold; text-align: center">Keadaan Memaksa (Force Majeur)</h4>

            <ol class="content-text style=margin-left: 40px;">
                <li>Yang dimaksud keadaan Force Majeur adalah berbagai keadaan yang mengganggu kelancaran pelaksanaan proyek seperti:

                <ol>
                    <li>Bencana alam (gempa bumi, tanah longsor, angin topan, banjir, kebakaran, dll) yang bisa menyebabkan terganggunya jalannya proses konstruksi.</li>

                    <li>Kebijaksanaan pemerintah di bidang moneter (devaluasi) atau kenaikan harga BBM yang mengakibatkan proyek tersebut terganggu secara teknis maupun anggaran biaya.</li>

                    <li>Peperangan atau huru-hara yang mengakibatkan proyek tidak bisa dilanjutkan.</li>
                </ol>



                </li>

                <li><b>PIHAK KEDUA</b> harus memberitahukan pada <b>PIHAK PERTAMA</b> tentang gangguan yang dimaksud beserta kendala dan akibat yang ditimbulkan paling lambat 2 x 24 jam terhitung sejak peristiwa tersebut terjadi, jika tidak maka akan dianggap tidak terjadi force majeur.</li>

                <li>Jika terjadi force majeur, <b>PIHAK KEDUA</b> harus memberikan itikad baik mengenai kelanjutan proyek.</li>

                <li>Dalam keadaan yang disebutkan dalam poin 1 (satu), maka kedua belah pihak bisa bermusyawarah untuk kesepakatan dalam memutuskan keberlanjutan proyek.</li>

            </ol>

            <h4 style="font-weight: bold; text-align: center;">PASAL 12</h4>
            <h4 style="font-weight: bold; text-align: center">Kewajiban PIHAK KEDUA</h4>

            <ol class="content-text style=margin-left: 40px;">
                <li>PIHAK KEDUA berkewajiban melaksanakan pekerjaan sesuai dengan spesifikasi yang telah tercantum pada desain, gambar kerja dan daftar kuantitas (Bill of Quantity)  yang sudah disepakati bersama.</li>

                <li>PIHAK KEDUA bisa mendatangkan, menambah dan/atau mengganti tenaga kerja tanpa sepengetahuan PIHAK PERTAMA jika diperlukan sebagaimana yang ditanggungjawabkan kepada PIHAK KEDUA</li>
            </ol>

